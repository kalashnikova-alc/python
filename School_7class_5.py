# Функція для обчислення довжини кола

def C(r):
    return 2 * pi * r  # Формула довжини кола: 2 * π * r

# Функція для обчислення площі круга


def S(r):
    return pi * r * r  # Формула площі круга: π * r²


# Введення трьох радіусів
r1 = float(input('r1 = '))  # Перший радіус
r2 = float(input('r2 = '))  # Другий радіус
r3 = float(input('r3 = '))  # Третій радіус

# Обчислення сумарної довжини кіл і площ кругів
SumC = C(r1) + C(r2) + C(r3)  # Сумарна довжина кіл
SumS = S(r1) + S(r2) + S(r3)  # Сумарна площа кругів

# Виведення результатів
# Довжина з округленням до 4 знаків після коми
print('Сумарна довжина кіл:', round(SumC, 4))
# Площа з округленням до 4 знаків після коми
print('Сумарна площа кругів:', round(SumS, 4))


# Функція для обчислення довжини кола та площі круга


def Cands(r):
    C = 2 * pi * r  # Обчислення довжини кола
    S = pi * r * r  # Обчислення площі круга
    return C, S  # Повертаємо два значення: довжину кола та площу круга


# Введення трьох радіусів
r1 = float(input('r1 = '))  # Перший радіус
r2 = float(input('r2 = '))  # Другий радіус
r3 = float(input('r3 = '))  # Третій радіус

# Виклик функції Cands для кожного радіуса
c1, s1 = Cands(r1)  # Довжина та площа для першого кола
c2, s2 = Cands(r2)  # Довжина та площа для другого кола
c3, s3 = Cands(r3)  # Довжина та площа для третього кола

# Обчислення сумарних значень
SumC = c1 + c2 + c3  # Сумарна довжина кіл
SumS = s1 + s2 + s3  # Сумарна площа кругів

# Виведення результатів
# Довжина з округленням до 4 знаків після коми
print('Сумарна довжина кіл:', round(SumC, 4))
# Площа з округленням до 4 знаків після коми
print('Сумарна площа кругів:', round(SumS, 4))
